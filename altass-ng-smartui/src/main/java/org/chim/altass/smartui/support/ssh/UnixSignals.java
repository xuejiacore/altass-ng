public class UnixSignals {
    /** Exit 	Hangup */
    public static final int SIGHUP = 1;
    /** Exit 	Interrupt */
    public static final int SIGINT = 2;
    /** Core 	Quit */
    public static final int SIGQUIT = 3;
    /** Core 	Illegal Instruction */
    public static final int SIGILL = 4;
    /** Core 	Trace/Breakpoint Trap */
    public static final int SIGTRAP = 5;
    /** Core 	Abort */
    public static final int SIGABRT = 6;
    /** Core 	Emulation Trap */
    public static final int SIGEMT = 7;
    /** Core 	Arithmetic Exception */
    public static final int SIGFPE = 8;
    /** Exit 	Killed */
    public static final int SIGKILL = 9;
    /** Core 	Bus Error */
    public static final int SIGBUS = 10;
    /** Core 	Segmentation Fault */
    public static final int SIGSEGV = 11;
    /** Core 	Bad System Call */
    public static final int SIGSYS = 12;
    /** Exit 	Broken Pipe */
    public static final int SIGPIPE = 13;
    /** Exit 	Alarm Clock */
    public static final int SIGALRM = 14;
    /** Exit 	Terminated */
    public static final int SIGTERM = 15;
    /** Exit 	User Signal 1 */
    public static final int SIGUSR1 = 16;
    /** Exit 	User Signal 2 */
    public static final int SIGUSR2 = 17;
    /** Ignore 	Child Status */
    public static final int SIGCHLD = 18;
    /** Ignore 	Power Fail/Restart */
    public static final int SIGPWR = 19;
    /** Ignore 	Window Size Change */
    public static final int SIGWINCH = 20;
    /** Ignore 	Urgent Socket Condition */
    public static final int SIGURG = 21;
    /** Ignore 	Socket I/O Possible */
    public static final int SIGPOLL = 22;
    /** Stop 	Stopped (signal) */
    public static final int SIGSTOP = 23;
    /** Stop 	Stopped (user) */
    public static final int SIGTSTP = 24;
    /** Ignore 	Continued */
    public static final int SIGCONT = 25;
    /** Stop 	Stopped (tty input) */
    public static final int SIGTTIN = 26;
    /** Stop 	Stopped (tty output) */
    public static final int SIGTTOU = 27;
    /** Exit 	Virtual Timer Expired */
    public static final int SIGVTALRM = 28;
    /** Exit 	Profiling Timer Expired */
    public static final int SIGPROF = 29;
    /** Core 	CPU time limit exceeded */
    public static final int SIGXCPU = 30;
    /** Core 	FileInfo size limit exceeded */
    public static final int SIGXFSZ = 31;
    /** Ignore 	All LWPs blocked */
    public static final int SIGWAITING = 32;
    /** Ignore 	Virtual Interprocessor Interrupt for Threads Library */
    public static final int SIGLWP = 33;
    /** Ignore 	Asynchronous I/O */
    public static final int SIGAIO = 34;
}
